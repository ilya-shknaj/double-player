package by.gravity.doublexplayer.installer.model;

public class DoublePlayerModel {

	private String appName;

	private int iconRecourse;

	private String contentInputPath;

	private String contentOutputPath;

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public int getIconRecourse() {
		return iconRecourse;
	}

	public void setIconRecourse(int iconRecourse) {
		this.iconRecourse = iconRecourse;
	}

	public String getContentInputPath() {
		return contentInputPath;
	}

	public void setContentInputPath(String contentPath) {
		this.contentInputPath = contentPath;
	}

	public String getContentOutputPath() {
		return contentOutputPath;
	}

	public void setContentOutputPath(String contentOutputPath) {
		this.contentOutputPath = contentOutputPath;
	}

}
